    private void doneBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_doneBtnActionPerformed
        if(!equipField.getText().equals("")){
            if(equipField.getText().length() <= 100){
                RPITechSuppCamViewer.addEquipmentToDB(equipField.getText());
                equipField.setText("");
                this.setVisible(false);
            }
            else{
                JOptionPane.showMessageDialog(null,"Total length must not exceed 100 characters. Current count: " + equipField.getText().length(),"Alert",JOptionPane.WARNING_MESSAGE);
            }
        }
        else{
            equipField.setText("");
            this.setVisible(false);
        }
    }//GEN-LAST:event_doneBtnActionPerformed

